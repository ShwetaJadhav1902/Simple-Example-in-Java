package com.fruit;

public class Fruit {
	private String fruitColor;
	private String fruitName;
	private String fruitTaste;
	
	public String getFruitColor() {
		return fruitColor;
	}
	public void setFruitColor(String color) {
		fruitColor=color;
	}
	public String getFruitName() {
		return fruitName;
	}
    public void setFruitName(String name) {
    	fruitName=name;
    }
    public String getFruitTaste() {
    	return fruitTaste;
    }
    public void setFruitTaste(String taste) {
    	fruitTaste=taste;
    }

}
